class Anggota {
    private String idAnggota;
    private String nama;
    private String noTelp;

    public Anggota(String idAnggota, String nama, String noTelp) {
        this.idAnggota = idAnggota;
        this.nama = nama;
    }


    public String getIdAnggota() {
        return idAnggota;
    }

    public String getNama() {
        return nama;
    }

    public String getNoTelp() {
        return noTelp;
    }


}
